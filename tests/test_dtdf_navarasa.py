# ─────────────────────────────────────────────────────────────────────────────
# HASYA DIMENSION: Graceful degradation, soft errors, non-fatal warnings
# These tests verify that the tool continues to function or fails gracefully
# when given imperfect but recoverable inputs.
# ─────────────────────────────────────────────────────────────────────────────

def test_hasya_unknown_response_charset_gives_error_not_crash(httpbin_both):
    """HASYA: An unknown charset should produce a readable error, not a traceback."""
    r = http(
        '--response-charset=not-a-real-charset',
        httpbin_both + '/get',
        tolerate_error_exit_status=True,
    )
    assert r.exit_status != ExitStatus.SUCCESS
    assert 'not supported' in r.stderr.lower() or 'encoding' in r.stderr.lower()


def test_hasya_invalid_mime_type_gives_useful_error(httpbin_both):
    """HASYA: An invalid mime type argument should degrade with a clear message."""
    r = http(
        '--response-mime=not/a/valid/mime',
        httpbin_both + '/get',
        tolerate_error_exit_status=True,
    )
    assert r.exit_status != ExitStatus.SUCCESS
    assert len(r.stderr) > 0


def test_hasya_max_headers_zero_means_no_limit(httpbin_both):
    """HASYA: --max-headers=0 is the documented way to disable the header limit."""
    r = http('--max-headers=0', httpbin_both + '/get')
    assert HTTP_OK in r


def test_hasya_verbose_flag_does_not_break_normal_output(httpbin_both):
    """HASYA: --verbose should add information without corrupting the response."""
    r = http('--verbose', httpbin_both + '/get')
    assert HTTP_OK in r
    assert 'GET /get' in r


def test_hasya_print_flag_with_only_body(httpbin_both):
    """HASYA: --print=b should produce body output without crashing on headers."""
    r = http('--print=b', httpbin_both + '/get')
    assert HTTP_OK not in r  # headers should be absent
    assert len(r) > 0        # but body should be present


